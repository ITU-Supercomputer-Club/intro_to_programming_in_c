#include <stdio.h>

int main() {

    /*
        Data type: int​
        Name: age​
        Initial value: Unknown (random). Compile without -Wall -Werror to see randomness!!
    */
    int age;
    printf("The age is: %d\n", age);

    /*
        Data type: double
        Name: temperature
        Initial value: 25.3

    */
    double temperature = 25.3;
    printf("The temperature is: %lf\n", temperature);

    return 0;
}

